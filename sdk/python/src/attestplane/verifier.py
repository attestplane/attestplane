"""High-level proof-bundle verifier.

Takes a bundle dict (or path to a bundle JSON file), validates against
``schemas/v1/proof_bundle.schema.json``, rehydrates the contained
events, re-walks the chain with :func:`~attestplane.hashchain.verify_chain`,
and returns a :class:`BundleVerificationResult`.

Verifiers are intentionally **pure** with respect to the bundle: they
do not mutate, sign, or anchor. Verification is read-only.
"""

